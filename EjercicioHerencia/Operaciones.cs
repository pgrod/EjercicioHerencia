namespace EjercicioHerencia
{
    public class Operaciones
    {
        public double CalcularArea(double lado)
        {
            return lado * lado;
        }
        public double CalcularPerimetro(double lado)
        {
            return 4 * lado;
        }
        public double CalcularVolumen(double lado)
        {
            return lado * lado * lado;
        }
    }
}
